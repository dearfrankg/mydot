#-------------------------------------------------------------------------
# show perl version for a module - ex: pv CGI
#-------------------------------------------------------------------------
pv() {
    perl -e "use $1 9999;"  2>&1 \
    | perl -ne ' ($a) = /(version [\d.]+) at/;  print "$a\n" if $a; '
}




